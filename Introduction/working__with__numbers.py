import random

numOne = 34
numTwo = 9303
numThree = 2
numfour = 995

# ADDITION
print(numOne + numThree)

# SUBTRACTION
print(numfour - numTwo)

# DIVISION
print(numTwo / numOne)

# MULTIPLICATION
print(numfour * numOne)

# MODULUS
print(numfour % numOne)

# BODMAS
print(numThree - numOne + (numfour - numTwo))

# GETTING A RANDOM NUMBER
print("Random Number -", random.randrange(100, 2000))