# what is a library in pythoxn 
# in a real world 
# a library is a collection of books or is a 
# room or place where many books are stored to be used later.

# in programming 
# a library is a collection of precompiled 
# codes that can be used later on in a program 
# for some specific well-defined operations.Other than pre-compiled codes, 
# a library may contain documentation, configuration data, message 
# templates, classes, and values, etc.

